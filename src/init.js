function isValidHex(value) {
  return /^#([0-9A-Fa-f]{3}|[0-9A-Fa-f]{6})$/.test(value.trim());
}

function validateHex(value) {
  if (!value) return 'Color is required';
  if (!isValidHex(value)) return 'Enter a valid hex color e.g. #2F48C4';
  return;
}

